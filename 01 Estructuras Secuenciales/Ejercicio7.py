numero1 = int(input("Ingresa un número entero distinto a 0: "))
numero2 = int(input("Ingresa otro número entero distinto a 0: "))
print(f"{numero1} + {numero2} = {numero1 + numero2}")
print(f"{numero1} / {numero2} = {numero1 / numero2}")
print(f"{numero1} * {numero2} = {numero1 * numero2}")
print(f"{numero1} - {numero2} = {numero1 - numero2}")